    public void jogadorDois() 
    {
        for (Ale = (int) ((Math.random() * 9) + 1); Ale != 10; Ale = (int) ((Math.random() * 9) + 1))
        {
            if (Ale == 1 && btn00.getText().isEmpty()) 
            {
                Jogar(btn00);
                break;
            }
            else if (Ale == 2 && btn01.getText().isEmpty()) 
            {
                Jogar(btn01);
                break;
            }
            else if (Ale == 3 && btn02.getText().isEmpty()) 
            {
                Jogar(btn02);
                break;
            }
            else if (Ale == 4 && btn10.getText().isEmpty()) 
            {
                Jogar(btn10);
                break;
            }
            else if (Ale == 5 && btn11.getText().isEmpty()) 
            {
                Jogar(btn11);
                break;
            }
            else if (Ale == 6 && btn12.getText().isEmpty()) 
            {
                Jogar(btn12);
                break;
            }
            else if (Ale == 7 && btn20.getText().isEmpty()) 
            {
                Jogar(btn20);
                break;
            }
            else if (Ale == 8 && btn21.getText().isEmpty()) 
            {
                Jogar(btn21);
                break;
            }
            else if (Ale == 9 && btn22.getText().isEmpty()) 
            {
                Jogar(btn22);
                break;
            }
        }
    }
    
