    private void embaralhar() {
        for (int i = 0; i <= 15; i++) {
            int x = (int) (Math.random() * 15);
            int temp = cartas[x];
            cartas[x] = cartas[i];
            cartas[i] = temp;
        }
    }

    public void Limpar() {
        for (int i = 0; i <= 15; i++) {
            botoes[i].setText("");
        }
        recorde();
        jogada = 0;
        embaralhar();
        jogadas = 0;
        Final = 0;
        lblJogadas.setText("00");
    }
    
    public void recorde() {
        if(Recorde < jogadas)
        {
            Recorde = jogadas;
            lblRecorde.setText("" + Recorde);
        }
    }
