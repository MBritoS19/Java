    public void Jogar(int x) {
        if (y != x) {
            if (jogada == 0) {
                botoes[x].setText("" + cartas[x]);
                j = cartas[x];
                y = x;
                jogada++;
                jogadas++;
                lblJogadas.setText("" + jogadas);
            } else {
                botoes[x].setText("" + cartas[x]);
                if (cartas[x] == j) {
                    JOptionPane.showMessageDialog(rootPane, "Acertou!");
                    botoes[x].setEnabled(false);
                    botoes[y].setEnabled(false);
                    Final++;
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Errou!");

                    botoes[x].setText("");
                    botoes[y].setText("");
                }
                jogada = 0;
                jogadas++;
                lblJogadas.setText("" + jogadas);
                y = -1;
            }
        }
        else
        {
           JOptionPane.showMessageDialog(rootPane, "Escolha uma op√ß√£o valida"); 
        }
        if(Final == 8)
            JOptionPane.showMessageDialog(rootPane, "Terminamos! recomece o jogo"); 
    }

