    private void Somar(int x, int botao) {
        String valorS = campos[x].getText();
        int total = 0;
        
        if (valorS != "") {
            int valor = Integer.parseInt(valorS);
            total = valor + botao;
        }
        else
        {
            total = botao;
        }

        if (total > 9) {
            total = 1;
        }

        campos[x].setText("" + total);
    }

