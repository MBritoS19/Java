    private void jogar(JLabel lbl) {
        if (btnNovo.isVisible())
            return;
        
        int pc = (int)(Math.random()*3);
        
        // habilitar o botão do pc        
        if (pc == 0){
            lblPedraPc.setEnabled(true);
        }
        else if (pc == 1){
            lblPapelPc.setEnabled(true);
        }
        else {
            lblTesouraPc.setEnabled(true);
        }
        
        // casos de vitória
        if ((pc == 0 && lbl.getToolTipText().equals("papel"))
                || (pc == 1 && lbl.getToolTipText().equals("tesoura"))
                || (pc == 2 && lbl.getToolTipText().equals("pedra"))
                ){
            lblResultado.setText("Ganhou");
            lbl.setIcon(new ImageIcon(getClass().getResource("/img/"+lbl.getToolTipText()+"_win.gif")));
            pontos++;
        }
        else if ((pc == 0 && lbl.getToolTipText().equals("pedra"))
                || (pc == 1 && lbl.getToolTipText().equals("papel"))
                || (pc == 2 && lbl.getToolTipText().equals("tesoura"))
                ){
            lblResultado.setText("Empate");
        }
        else{
            lblResultado.setText("Perdeu");
            derrotas++;
        }
        
        lblVitorias.setText(""+pontos);
        lblDerrotas.setText(""+derrotas);
        btnNovo.setVisible(true);
    }
